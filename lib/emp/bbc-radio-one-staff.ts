export function isBbcRadioOneSunderlandEvent(value: string) {
  const normalized = value.toLowerCase()
  return normalized.includes('bbc radio 1') && normalized.includes('big weekend') && normalized.includes('sunderland')
}

export function getBbcRadioOneStaffForEvent(eventName: string, planTitle = '') {
  return isBbcRadioOneSunderlandEvent(`${eventName} ${planTitle}`) ? BBC_RADIO_ONE_2026_STAFF : []
}

export function normalizeEmpStaffName(value: string) {
  return value.toLowerCase().replace(/[^a-z\s]/g, ' ').replace(/\s+/g, ' ').trim()
}

export function findEmpStaffByName(rows: EmpStaffSignInRow[], staffName: string) {
  const normalizedName = normalizeEmpStaffName(staffName)
  if (!normalizedName) return null

  return rows.find((row) => normalizeEmpStaffName(row.staffName) === normalizedName) || null
}
